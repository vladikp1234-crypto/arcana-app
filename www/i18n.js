// Get the language to use — saved preference, browser language, or English
function getCurrentLang() {
  const saved = localStorage.getItem('arcana_lang');
  if (saved && TRANSLATIONS[saved]) return saved;
  const browserLang = (navigator.language || 'en').slice(0, 2);
  if (TRANSLATIONS[browserLang]) return browserLang;
  return 'en';
}

function t(key, replacements = {}) {
  const lang = getCurrentLang();
  let str = TRANSLATIONS[lang][key] || TRANSLATIONS.en[key] || key;
  for (const [k, v] of Object.entries(replacements)) {
    str = str.replace(`{${k}}`, v);
  }
  return str;
}

function getAILang() {
  const lang = getCurrentLang();
  return TRANSLATIONS[lang].aiLang;
}

// Apply translations to DOM
function applyTranslations() {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.dataset.i18n;
    el.textContent = t(key);
  });
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    el.placeholder = t(el.dataset.i18nPlaceholder);
  });
}

// Build language picker
function buildLanguagePicker() {
  const picker = document.getElementById('langPicker');
  if (!picker) return;
  const current = getCurrentLang();
  picker.innerHTML = '';
  Object.values(TRANSLATIONS).forEach(lang => {
    const btn = document.createElement('button');
    btn.className = 'lang-option' + (lang.code === current ? ' active' : '');
    btn.innerHTML = `${lang.flag} ${lang.name}`;
    btn.onclick = () => {
      localStorage.setItem('arcana_lang', lang.code);
      location.reload();
    };
    picker.appendChild(btn);
  });
}
